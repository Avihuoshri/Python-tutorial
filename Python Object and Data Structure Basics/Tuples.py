#count('var') method - counts how many time var appears int the tuple
some_tuple = ('a', 'b', 'a', 'a')
print(some_tuple.count('a')) # 'a' appears 3 time in some_tuple

#index('var') method - return the first index that var appears
print(some_tuple.index('a')) # 'a' appears 3 time in some_tuple